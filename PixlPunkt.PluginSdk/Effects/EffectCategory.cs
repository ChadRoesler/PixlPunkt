namespace PixlPunkt.PluginSdk.Effects
{
    /// <summary>
    /// Categories for organizing effects in the UI.
    /// </summary>
    public enum EffectCategory
    {
        /// <summary>
        /// Stylize effects that change the overall look (shadows, outlines, glow).
        /// </summary>
        Stylize,

        /// <summary>
        /// Filter effects that process pixels (blur, grain, scanlines).
        /// </summary>
        Filter,

        /// <summary>
        /// Color effects that modify colors (adjust, quantize, tint).
        /// </summary>
        Color
    }
}
